    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        String search = txtSearch.getText();

        List<Category> categorys = new ArrayList<>();
        try {
            
        
            if (rdoId.isSelected()) {
                categorys.clear();
                categorys.add(categoryService.findCategoryById(Long.parseLong(search)));
                fillTable(categorys);
            } else if (rdoName.isSelected()) {
                categorys.clear();
                fillTable(categoryService.findCategoryByName(search));
            } else {
                if (txtSearch.getText() == "") {
                    JOptionPane.showMessageDialog(null, "Debe llenar el cuadro de texto");
                }
            }
            
            } 
        catch (Exception e) {
            JOptionPane.showMessageDialog(null, "No se han encontrado categorias");
        }

        


    }//GEN-LAST:event_btnSearchActionPerformed

